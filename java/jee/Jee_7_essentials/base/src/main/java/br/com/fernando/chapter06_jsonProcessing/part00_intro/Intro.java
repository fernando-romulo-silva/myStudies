package br.com.fernando.chapter06_jsonProcessing.part00_intro;

public class Intro {

    // JSON (JavaScript Object Notation) is a lightweight data-interchange format.
    // The format is easy for humans and machines to read and write.
    //
    // A JSON structure can be built as either of the following:
    //
    // * A collection of name/value pairs, generally realized as dictionary, hash table, or associative array
    //
    // * An ordered list of values, generally realized as an array, list, or sequence
    //
    // The following example shows the JSON representation of an object that describes a movie:
    //
    /**
     * <pre>
     * {  
     *  "name": "The Matrix",
     *  "actors": [
     *              "Keanu Reeves",
     *              "Laurence Fishburne",
     *              "Carrie-Ann Moss"
     *             ],
     *  "year": 1999
     * }
     * </pre>
     */

    // The object has three name/value pairs.
    // The first name is name with a string value for the movie name, the second name is actors with an array value
    // for the actors in the movie, and the third name is year with a number value for the year the movie was released.
}
