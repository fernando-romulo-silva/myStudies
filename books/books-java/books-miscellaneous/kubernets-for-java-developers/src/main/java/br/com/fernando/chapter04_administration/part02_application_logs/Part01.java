public class Part01 {

    // The kubectl logs command prints standard output and standard error output from the container(s) in a pod.
    //
    // If the pod consists of multiple containers, the container name must be specified—for example, kubectl logs <pod-name> <container-name>.
    //
    // Kubernetes supports cluster-level logging that allows you to collect, manage, and query the logs of an application composed of multiple pods.
    //
    // Cluster-level logging allows you to collect logs that persist beyond the lifetime of the pod’s container images or the lifetime of the pod or even cluster.
}
