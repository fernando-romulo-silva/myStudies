package br.com.fernando.chapter02_deploying_java_app_to_kubernetes.part01_development_cluster_minikube;

public class Part01 {

    // Minikube runs a single-node Kubernetes cluster inside a VM on your pc.
    // This allows you to try out Kubernetes on your local machine easily.
    //
    // Start cluster
    //
    // $ minikube start
    //
    //
    // For more details about client and server version, use the command kubectl version.
    //
    // $ kubectl version
    //
    //
    // Minikube common commands:
    //
    // stop - Stops a running local Kubernetes cluster
    //
    // delete - Deletes a local Kubernetes cluster
    //
    //
    // Kubernetes dashboard
    //
    // The Kubernetes dashboard is a general-purpose, web-based UI for Kubernetes clusters.
    //
    // It provides an overview of applications running on the cluster, as well as allowing you to create or modify individual Kubernetes resources and workloads,
    // such as replica sets, jobs, services, and pods.
    //
    // The dashboard can be used to manage the cluster as well.
    //
    // $ minikube dashboard

}
