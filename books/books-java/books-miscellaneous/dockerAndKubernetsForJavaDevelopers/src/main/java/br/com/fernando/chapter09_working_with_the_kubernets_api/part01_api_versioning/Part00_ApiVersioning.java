public class Part00_ApiVersioning {

	// Kubernetes grows continuously.
	// Its features change and this results in the API changing as well.
	// To deal with those changes and to not break compatibility with existing clients over an extended period of time, Kubernetes supports multiple API versions,
	// each with a different API path, such as /api/v1 or /apis/extensions/v1beta1.
	//
	// There are three API levels in the Kubernetes API specification: alpha, beta, and stable.

}
