package br.com.fernando.chapter08_using_kubernets_with_java.part01_installing;

public class Part01_StartingUpLocalKubernets {

	// Starting up the local Kubernetes cluster
	//
	// Minikube works on its own virtual machine. Depending on your host OS, you can choose between several virtualization drivers.
	//
	// Currently supported are virtualbox, vmwarefusion, xhyve, hyperv, and kvm (Kernel-based virtual machine).
	// The default VM driver is virtual box. You can override this option. 
	// This is the example macOS startup command line which uses xhyve:
	//
	// $ minikube start --vm-driver=xhyve
	//
	//
	// As the cluster is running now and we have the dashboard addon enabled by default, you can take a look at the (still empty) Kubernetes dashboard with the following command:
	//
	// $ minikube dashboard
	
}
