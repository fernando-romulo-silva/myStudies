package br.com.fernando.chapter10_deploying_java_on_kubernetes_in_the_cloud.part02_installing_the_tools;

public class Part01_python_and_pip {

	// Python and PIP
	//
	// To run the AWS command-line tools (awscli), we will need python3 present on our machine.
	// It may be present already, you can verify that using the command:
	//
	// $ python3 --version
	//
	//
	// Once Python is installed, we will need another tool, which is pip. pip is the recommended tool for installing Python packages. 
	// It's written in Python itself. 
	// You can install it using the package manager of your choice, executing the following, for example, on Ubuntu or Debian:
	//
	// $ sudo apt-get install python3-pip   
	//
	// 
	// After a while, pip should be available for you in the terminal shell. 
	// To verify if it's working, execute the following command:
	//
	// $ pip -V
	//
	// or
	//
	// $ pip3 -V
	
}
