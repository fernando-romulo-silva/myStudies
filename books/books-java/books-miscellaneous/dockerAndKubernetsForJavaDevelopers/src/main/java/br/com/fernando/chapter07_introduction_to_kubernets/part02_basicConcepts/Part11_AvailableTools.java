package br.com.fernando.chapter07_introduction_to_kubernets.part02_basicConcepts;

public class Part11_AvailableTools {

	// kubectl
	//
	// kubectl is a command-line interface for running commands against Kubernetes clusters. 
	// In fact, this is the command used most often when working with Kubernetes.
	//
	//
	// Dashboard
	//
	// Kubernetes Dashboard is a nice, clean web-based UI for Kubernetes clusters. 
	// Using the Dashboard, you can manage and troubleshoot the cluster itself as well as the applications running in it. 
	// You could say it's the Kubernetes user interface.
	//
	//
	// Minikube 
	//
	// The minikube tool, available at GitHub at https://github.com/kubernetes/minikube, allows you to set up the local cluster on your own machine. 
	// It's available for all major platforms
	
}
