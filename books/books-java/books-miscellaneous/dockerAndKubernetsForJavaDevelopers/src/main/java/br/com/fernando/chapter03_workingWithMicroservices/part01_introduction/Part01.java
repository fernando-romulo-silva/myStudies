package br.com.fernando.chapter03_workingWithMicroservices.part01_introduction;

public class Part01 {

    // Microservice Architecture (MSA), is an architectural style and design pattern which says that an application should consist of a collection of loosely-coupled services.
    //
    // This architecture decomposes business domain models into smaller, consistent pieces implemented by services.
    //
    // In other words, each of the services will have its own responsibilities, independent of others, each one of them will provide a specific functionality.
    //
    // These services should be isolated and autonomous. Yet, they of course need to communicate to provide some piece of business functionality.
    //
    // They usually communicate using REST exposures or by publishing and subscribing events in the publish/subscribe way.

}
