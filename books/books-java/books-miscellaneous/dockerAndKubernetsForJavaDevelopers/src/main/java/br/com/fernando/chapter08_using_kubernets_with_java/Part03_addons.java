package br.com.fernando.chapter08_using_kubernets_with_java;

public class Part03_addons {
	
	// Minikube comes with several add-ons, such as Kubernetes dashboard, Kubernetes DNS, and so on. 
	// We can list the available addons by executing the following command:
	//
	// $ minikube addons list 
	//
	// To enable or disable the addon, we use minikube addons disable or minikube addons enable, respectively, for example:
	//
	// $ minikube addons disable dashboard
	//
	// $ minikube addons enable heapster  

}
